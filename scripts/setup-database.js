// Script to set up the initial database schema
require('dotenv').config({ path: '.env' });

// Use the service role key for server-side operations
const { createClient } = require('@supabase/supabase-js');

const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL || '';
const supabaseServiceRoleKey = process.env.SUPABASE_SERVICE_ROLE_KEY || '';

if (!supabaseUrl || !supabaseServiceRoleKey) {
  console.error('Missing Supabase environment variables for setup');
  process.exit(1);
}

// Create a Supabase client with service role key for server-side operations
const supabaseAdmin = createClient(supabaseUrl, supabaseServiceRoleKey, {
  auth: {
    persistSession: false,
    autoRefreshToken: false,
    detectSessionInUrl: false,
  },
});

async function setupDatabase() {
  try {
    console.log('Setting up database schema...');
    
    // For Supabase, we can't create tables directly via the JS client
    // We'll instead verify that we can connect and provide instructions
    console.log('‚úÖ Connected to Supabase successfully');
    console.log('\nüìù IMPORTANT: Please create the required tables in your Supabase SQL editor:\n');
    console.log('1. Go to your Supabase Dashboard');
    console.log('2. Open the SQL Editor');
    console.log('3. Run the following queries one by one:\n');
    
    console.log('-- Create profiles table');
    console.log(`CREATE TABLE IF NOT EXISTS profiles (
  id UUID REFERENCES auth.users NOT NULL PRIMARY KEY,
  updated_at TIMESTAMP WITH TIME ZONE,
  username TEXT UNIQUE,
  full_name TEXT,
  avatar_url TEXT,
  website TEXT,
  CONSTRAINT username_length CHECK (CHAR_LENGTH(username) >= 3)
);

-- Set up Row Level Security (RLS)
ALTER TABLE profiles ENABLE ROW LEVEL SECURITY;

-- Policies
CREATE POLICY "Public profiles are viewable by everyone." ON profiles
  FOR SELECT USING (true);

CREATE POLICY "Users can insert their own profile." ON profiles
  FOR INSERT WITH CHECK (auth.uid() = id);

CREATE POLICY "Users can update own profile." ON profiles
  FOR UPDATE USING (auth.uid() = id);
`);
    
    console.log('-- Create rooms table');
    console.log(`CREATE TABLE IF NOT EXISTS rooms (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::TEXT, NOW()) NOT NULL,
  name TEXT NOT NULL
);

-- Set up Row Level Security (RLS)
ALTER TABLE rooms ENABLE ROW LEVEL SECURITY;

-- Policies
CREATE POLICY "Rooms are viewable by everyone." ON rooms
  FOR SELECT USING (true);
`);
    
    console.log('-- Create messages table');
    console.log(`CREATE TABLE IF NOT EXISTS messages (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::TEXT, NOW()) NOT NULL,
  room_id BIGINT REFERENCES rooms NOT NULL,
  user_id UUID REFERENCES auth.users NOT NULL,
  content TEXT NOT NULL
);

-- Set up Row Level Security (RLS)
ALTER TABLE messages ENABLE ROW LEVEL SECURITY;

-- Policies
CREATE POLICY "Messages are viewable by everyone." ON messages
  FOR SELECT USING (true);

CREATE POLICY "Authenticated users can insert messages." ON messages
  FOR INSERT WITH CHECK (auth.role() = 'authenticated');
`);
    
    console.log('\n‚úÖ After running these queries, run the seed script with: npm run seed');
    
    return { success: true };
  } catch (error) {
    console.error('Error setting up database:', error);
    return { success: false, error: error.message };
  }
}

async function run() {
  console.log('Starting database setup process...');
  
  try {
    const result = await setupDatabase();
    
    if (result.success) {
      console.log('\n‚úÖ Database setup instructions provided successfully!');
      process.exit(0);
    } else {
      console.error('‚ùå Database setup failed:', result.error);
      process.exit(1);
    }
  } catch (error) {
    console.error('‚ùå Unexpected error during database setup:', error);
    process.exit(1);
  }
}

run();